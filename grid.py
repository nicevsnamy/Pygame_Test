class Grid:
    def __init__(self):
        self.height = 20
        self.width = 20
        self.cell_height = 100
        self.cell_width = 100
        self.thickness = 3
    def get_height(self):
        return self.height
    def get_width(self):
        return self.width
    def get_cell_height(self):
        return self.cell_height
    def get_cell_width(self):
        return self.cell_width
    def get_thickness(self):
        return self.thickness